export const tagColor = (tag) => {
  if (tag === "Tech") {
    return "bg-blue-600";
  }
  if (tag === "Pinia") {
    return "bg-yellow-600";
  }
  if (tag === "JavaScript") {
    return "bg-red-600";
  }
  if (tag === "Vue 3") {
    return "bg-green-600";
  }
  if (tag === "Template") {
    return "bg-gray-500";
  }
};
